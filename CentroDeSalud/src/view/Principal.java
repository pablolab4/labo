    public boolean validation(){
        if (txtNombreVisitante.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "El campo Nombre del visitante está vacío");
            txtNombreVisitante.requestFocus();
            return false;
        }
        if (txtDuracionVisita.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "El campo Duración Visita está vacío");
            txtDuracionVisita.requestFocus();
            return false;
        }
        
      
        String text = txtDuracionVisita.getText();
        try {
            int duracionVisita = Integer.parseInt(text);
            
            if(duracionVisita <= 0){
            JOptionPane.showMessageDialog(null, "La duracion debe ser mayor a 0");
            txtDuracionVisita.requestFocus();
            return false;
        }
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(null, "El campo duracion debe ser un número");
            txtDuracionVisita.requestFocus();
        }
        return true;
    }

