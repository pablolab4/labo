    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if(validation()){
            String nombreVisitante = txtNombreVisitante.getText();
            Paciente paciente = (Paciente)cbxPaciente.getSelectedItem();
            Empleado empleado = (Empleado)cbxRecepcionista.getSelectedItem();
            int duracion = Integer.parseInt(txtDuracionVisita.getText());

            Visita v = new Visita();
            v.setPaciente(paciente);
            v.setEmpleado(empleado);
            v.setNombre(nombreVisitante);
            v.setDuracion(duracion);

            gestor.addVisita(v);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

